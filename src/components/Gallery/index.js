class Gallery extends Component {
  state = {isSelect: 0}

  selectedImage = id => {
    this.setState({isSelect: id})
  }

  render() {
    const {isSelect} = this.state
    const filterThumbnail = imagesList.filter(each => each.id === isSelect)
    console.log(filterThumbnail)

    return (
      <div className="bgContainer">
        {filterThumbnail.map(each => (
          <img
            className="thumbnail"
            src={each.imageUrl}
            alt={each.imageAltText}
          />
        ))}
        <div>
          <h1>Nature Photography</h1>
          <p>Nature Photography by Rahul</p>
          <ul className="ulist">
            {imagesList.map(eachThumb => (
              <Thumbnail
                key={eachThumb.id}
                eachThumb={eachThumb}
                selectedImage={this.selectedImage}
                isActive={eachThumb.id === isSelect}
              />
            ))}
          </ul>
        </div>
      </div>
    )
  }
}
export default Gallery
